ALTER SESSION
SET
    NLS_TIMESTAMP_FORMAT = 'YYYY-MM-DD HH24:MI:SS';

CREATE TABLE
    DAILY_MEETING (
        ID INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        TOPIC VARCHAR(255) NOT NULL,
        LOCATION VARCHAR(255) NOT NULL,
        MEETING_DATETIME TIMESTAMP NOT NULL,
        DURATION INTEGER NOT NULL,
        COMPANIES CLOB NOT NULL CHECK (COMPANIES IS JSON),
        COMPANY_PARTICIPANTS CLOB NOT NULL CHECK (COMPANY_PARTICIPANTS IS JSON),
        DEPARTMENTS CLOB NOT NULL CHECK (DEPARTMENTS IS JSON),
        DEPARTMENT_PARTICIPANTS CLOB NOT NULL CHECK (DEPARTMENT_PARTICIPANTS IS JSON),
        APPROVAL_AUTHORITIES CLOB NOT NULL CHECK (APPROVAL_AUTHORITIES IS JSON),
        APPROVAL_DATETIME TIMESTAMP NOT NULL
    );